// Assignment Operators
let x = 12; // Assignment operator (=)
let y = 5;
x += y; // x = x + y
console.log("x += y", x);

x -= y; // x = x - y
console.log("x -= y", x);

x *= y; // x = x * y
console.log("x *= y", x);

x /= y; // x = x / y
console.log("x /= y",x);

x %= y; // x = x % y
console.log("x %= y",x);

x **= y; // x = x ** y (Exponentiation assignment)

console.log("x **= y",x);
